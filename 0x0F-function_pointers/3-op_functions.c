#include "3-calc.h"
/**
 * op_add - adds two numbers
 * @a: Input Integer
 * @b: Input Integer
 * Return: Integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subs two numbers
 * @a: Input Integer
 * @b: Input Integer
 * Return: Integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiples two numbers
 * @a: Input Integer
 * @b: Input Integer
 * Return: Integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: Input Integer
 * @b: Input Integer
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulous on a using b
 * @a: input number
 * @b: input number
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
